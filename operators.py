# Arithmetic operators - Simple calculations

num1 = 56
num2 = 8

print(num1 + num2)
print(num1 - num2)
print(num1 * num2)
print(num1 / num2)
print(num1 % num2) # Modulus returns the remainder after dividing

#  Comparison Operators - Compare values
print(num1 < num2)
print(num1 > num2)
print(num1 <= num2)
print(num1 >= num2)
print(num1 == num2) # Equal to
print(num1 != num2) # Not equal to


# Assingnment operators - Assign values to variables
a = 200
print(a)


a += 20
print(a)


# Logic operators - and, or, not
x = 100
print(x < 250 and x > 1000)
print(x < 250 or x > 1000)
print(not(x < 250 or x > 1000))

# Operator predence - order in which operations get executed

output = (100-4 * 3 / 1)
print(int(output))


